    "• Never attempt to write or modify pages via tools.\n"
    "• Inspect `truncated` on listy results; widen `limit` when it's true.\n\n"
    "GOAL\n"
    "Provide accurate, verified MediaWiki syntax and structure aimed at the current editing task."
